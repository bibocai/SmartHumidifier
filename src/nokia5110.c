void lcdDC(uchar dt,uchar command)
{
	uchar i = 0;
	CE = 0;
	if(command == 0)
		DC = 0;
	else DC = 1;
	for(i;i<8;i++)
	{
		if(dt & 0x80)
			DIN = 1;
		else
			DIN = 0;
		CLK = 0;
		dt = dt << 1;
		CLK = 1;
	}
	CE = 1;
}
void lcdClear(void)
{
	uchar t = 0,k = 0;
	lcdDC(0x0C,0);
	lcdDC(0x80,0);
	for(t = 0;t < 6;t ++)
	{
		for(k = 0;k < 84;k ++)
		{
			lcdDC(0x00,1);
		}
	}
}
